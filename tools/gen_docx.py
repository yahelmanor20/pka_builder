def base_doc():
    doc = Document(TEMPLATE)
    body = doc.element.body
    sect = body.find(qn("w:sectPr"))
    for ch in list(body):
        if ch is not sect:
            body.remove(ch)
    if sect is not None and sect.find(qn("w:bidi")) is None:
        sect.insert(0, _el("w:bidi"))
    return doc


