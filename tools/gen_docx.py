def _landscape():
    from docx.enum.section import WD_ORIENT
    doc = Document()
    st = doc.styles["Normal"]; st.font.name = FONT; st.font.size = Pt(11)
    rpr = st.element.get_or_add_rPr(); rf = rpr.find(qn("w:rFonts"))
    if rf is None:
        rf = _el("w:rFonts"); rpr.insert(0, rf)
    rf.set(qn("w:ascii"), FONT); rf.set(qn("w:hAnsi"), FONT); rf.set(qn("w:cs"), FONT)
    sec = doc.sections[0]; sec._sectPr.append(_el("w:bidi"))
    w, h = sec.page_width, sec.page_height
    sec.orientation = WD_ORIENT.LANDSCAPE; sec.page_width, sec.page_height = h, w
    for m in ("left_margin", "right_margin", "top_margin", "bottom_margin"):
        setattr(sec, m, Pt(28))
    return doc


