GRAY_HEAD, GRAY_DARK = "595959", "333333"   # כותרת-עמודות / כותרת-יום (טקסט לבן)
ACCENT = RGBColor(0x8C, 0x3A, 0x3A)         # בורדו (היה כחול 1F4E79)
WHITE = RGBColor(0xFF, 0xFF, 0xFF)
