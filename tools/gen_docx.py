def _borders(t):
    b = _el("w:tblBorders")
    for e in ("top", "left", "bottom", "right", "insideH", "insideV"):
        b.append(_el("w:" + e, **{"w:val": "single", "w:sz": "4", "w:space": "0", "w:color": "000000"}))
    t._tbl.tblPr.append(b)


def _full_width(t, weights):
    tblPr = t._tbl.tblPr
    tw = tblPr.find(qn("w:tblW"))
    if tw is None:
        tw = _el("w:tblW"); tblPr.append(tw)
    tw.set(qn("w:w"), str(FULL_W)); tw.set(qn("w:type"), "dxa")
    tblPr.append(_el("w:tblLayout", **{"w:type": "fixed"}))
    s = float(sum(weights))
    widths = [int(FULL_W * w / s) for w in weights]
    grid = t._tbl.find(qn("w:tblGrid"))
    for c, wd in zip(grid.findall(qn("w:gridCol")), widths):
        c.set(qn("w:w"), str(wd))
    return widths


def grid_table(doc, headers, rows, weights=None):
    t = doc.add_table(rows=1, cols=len(headers))
    t.alignment = WD_TABLE_ALIGNMENT.RIGHT
    t._tbl.tblPr.append(_el("w:bidiVisual")); _borders(t)
    for j, h in enumerate(headers):
        _cell(t.rows[0].cells[j], h, bold=True, size=10, fill=GRAY_HEAD, color=WHITE, center=True); _vcenter(t.rows[0].cells[j])
    for row in rows:
        cells = t.add_row().cells
        for j, v in enumerate(row):
            _cell(cells[j], v, size=10); _vcenter(cells[j])
    _full_width(t, weights or [1] * len(headers))
    return t


def day_table(doc, day_title, headers, rows, weights=None, fill=False):
    n = len(headers)
    t = doc.add_table(rows=1, cols=n)
    t.alignment = WD_TABLE_ALIGNMENT.RIGHT
    t._tbl.tblPr.append(_el("w:bidiVisual")); _borders(t)
    m = t.rows[0].cells[0]
    for c in t.rows[0].cells[1:]:
        m = m.merge(c)
    _cell(m, day_title, bold=True, size=11, fill=GRAY_DARK, color=WHITE, center=True); _vcenter(m)
    hr = t.add_row().cells
    for j, h in enumerate(headers):
        _cell(hr[j], h, bold=True, size=10, fill=GRAY_HEAD, color=WHITE, center=True); _vcenter(hr[j])
    for row in rows:
        cells = t.add_row().cells
        for j, v in enumerate(row):
            _cell(cells[j], v, size=10); _vcenter(cells[j])
    _full_width(t, weights or [1] * n)
    if fill:
        _fill_height(t)
    return t


def _fill_height(t, target=13400):
    """מותח את שורות הטבלה כך שתמלא את גובה הדף (יישור אנכי כבר מוגדר)."""
    rows = t.rows
    if len(rows) < 3:
        return
    rows[0].height = Twips(560); rows[0].height_rule = WD_ROW_HEIGHT_RULE.AT_LEAST
    rows[1].height = Twips(440); rows[1].height_rule = WD_ROW_HEIGHT_RULE.AT_LEAST
    nd = len(rows) - 2
    h = int(max(540, min(1500, (target - 1000) / nd)))
    for r in rows[2:]:
        r.height = Twips(h); r.height_rule = WD_ROW_HEIGHT_RULE.AT_LEAST


