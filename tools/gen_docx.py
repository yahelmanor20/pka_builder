def _el(tag, **a):
    e = OxmlElement(tag)
    for k, v in a.items():
        e.set(qn(k), v)
    return e


def _run(run, size=11, bold=False, color=None):
    run.font.name = FONT; run.font.size = Pt(size); run.font.bold = bold
    run.font.color.rgb = color if color is not None else BODY
    rPr = run._element.get_or_add_rPr()
    rf = rPr.find(qn("w:rFonts"))
    if rf is None:
        rf = _el("w:rFonts"); rPr.insert(0, rf)
    rf.set(qn("w:ascii"), FONT); rf.set(qn("w:hAnsi"), FONT); rf.set(qn("w:cs"), FONT)
    rPr.append(_el("w:rtl", **{"w:val": "1"}))
    rPr.append(_el("w:szCs", **{"w:val": str(int(size * 2))}))


def _prtl(p, align=WD_ALIGN_PARAGRAPH.RIGHT):
    p._p.get_or_add_pPr().insert(0, _el("w:bidi"))
    p.alignment = align


def _shade(cell, fill):
    cell._tc.get_or_add_tcPr().append(_el("w:shd", **{"w:val": "clear", "w:color": "auto", "w:fill": fill}))


def _cell(cell, text, bold=False, size=10, fill=None, color=None, center=False):
    if fill:
        _shade(cell, fill)
    cell.text = ""
    al = WD_ALIGN_PARAGRAPH.CENTER if center else WD_ALIGN_PARAGRAPH.RIGHT
    p = cell.paragraphs[0]; _prtl(p, al)
    for i, line in enumerate(str(text).split("\n")):
        if i:
            p = cell.add_paragraph(); _prtl(p, al)
        _run(p.add_run(line), size=size, bold=bold, color=color)


def _vcenter(cell):
    cell._tc.get_or_add_tcPr().append(_el("w:vAlign", **{"w:val": "center"}))


# ---- בלוקי טקסט ----
def hdr(doc, text, bold=False, size=10):
    p = doc.add_paragraph(); _prtl(p); _run(p.add_run(text), size=size, bold=bold)


def title(doc, text, size=13, space_before=6):
    p = doc.add_paragraph(); _prtl(p)
    p.paragraph_format.space_before = Pt(space_before); p.paragraph_format.space_after = Pt(3)
    _run(p.add_run(text), size=size, bold=True, color=ACCENT)


def bullet(doc, text, size=10.5):
    p = doc.add_paragraph(); _prtl(p)
    p.paragraph_format.right_indent = Twips(240)
    p.paragraph_format.space_after = Pt(1)
    _run(p.add_run("•  " + text), size=size)


def plain(doc, text, size=10.5, bold=False):
    p = doc.add_paragraph(); _prtl(p); _run(p.add_run(text), size=size, bold=bold)


